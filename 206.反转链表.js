/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var reverseList = function(head) {
    if (!head) return null
    let res = null
    while(head) {
        res = {
            val: head.val,
            next: res
        }
        head = head.next
    }
    return res
};
